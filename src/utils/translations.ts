export const getFloorName = (floorNum: number, lang: 'en' | 'bn'): string => {
  if (lang === 'bn') {
    const banglaOrdinals: { [key: number]: string } = {
      1: '১ম',
      2: '২য়',
      3: '৩য়',
      4: '৪র্থ',
      5: '৫ম',
      6: '৬ষ্ঠ',
      7: '৭ম',
      8: '৮ম',
      9: '৯ম'
    };
    return `${banglaOrdinals[floorNum] || floorNum} তলা`;
  } else {
    const englishOrdinals: { [key: number]: string } = {
      1: '1st',
      2: '2nd',
      3: '3rd',
      4: '4th',
      5: '5th',
      6: '6th',
      7: '7th',
      8: '8th',
      9: '9th'
    };
    return `${englishOrdinals[floorNum] || `${floorNum}th`} Floor`;
  }
};

